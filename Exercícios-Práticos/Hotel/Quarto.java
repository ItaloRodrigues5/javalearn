public class Quarto {
    private int num;
    private double preco;

    public Quarto(int num, double preco) {
        this.num = num;
        this.preco = preco;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public double getPreco() {
        return preco;
    }

    public void setPreco(double preco) {
        this.preco = preco;
    }
}
